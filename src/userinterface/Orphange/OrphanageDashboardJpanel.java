        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a request");
        }

        DefaultTableModel model = (DefaultTableModel) tblNeeds.getModel();
        model.setRowCount(0);
        model.setRowCount(0);
        WorkRequest request = (WorkRequest) tblDonationReq.getValueAt(selectedRow, 0);

        ArrayList<Need> needList = ((DonationRequest) request).getNeedList();
