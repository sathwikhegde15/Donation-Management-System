    public void populateRequestTable() {
        DefaultTableModel model = (DefaultTableModel) tblDonationReq.getModel();

        model.setRowCount(0);
        for (WorkRequest request : userAccount.getWorkQueue().getWorkRequestList()) {

            // if (productList!=null){
            //for (Products p : productList) {
            Object[] row = new Object[4];
            row[0] = request;
            row[1] = request.getReceiver();
            row[2] = request.getStatus();
            String result = ((DonationRequest) request).getStatus();
            row[3] = result == null ? "Waiting" : result;

            model.addRow(row);

        }

    }

    //adding below method to get product data
    public void populateData() {

        DefaultTableModel model = (DefaultTableModel) tblNeeds.getModel();

        model.setRowCount(0);
        model.setRowCount(0);
        for (WorkRequest request : userAccount.getWorkQueue().getWorkRequestList()) {
            ArrayList<Need> needList = ((DonationRequest) request).getNeedList();
            if (needList != null) {
                for (Need n : needList) {
                    Object row[] = new Object[3];
                    row[0] = n;
                    row[1] = n.getNeedName();
                    row[2] = n.getQuantity();
                    ((DefaultTableModel) tblNeeds.getModel()).addRow(row);
                }
            }
        }

    }

